<p>你支持 LGBTQ+嗎？花點時間，認真的想想。<br/>
你為什麼支持 LGBTQ+？人權？朋友？還是什麼？ 你能支持它的一切嗎？你，了解它嗎？<br/>
LGBTQ+：L 代表 Leabian，女同志；G 代表 Gay，男同志；B 代表 Bisexual，雙性戀；T 代表 Transgender，跨性別；Q 代表 Questioning，疑問者，也可以代表 Queer（酷兒）；+代表其他更多非異性戀之性向。<br/>
這些是基本理解，更深層的法律問題、權利保障：同性性行為、跨國同婚等。還有很多是你們不了解的。</p>

<p>幾個禮拜前，我和健康老師討論「性取向問題 Sexual Orientation」<br/>
跟老師談了一整節課，覺得很感動，彷彿她才是輔導老師……<br/>
這是她說的一句話：「勇敢為自己所要的，珍惜自己所有的。」<br/>
只不過，最近周遭的事，又讓我想去找健康老師了。<br/>
週四要技藝班，健康課常上不到，所以我跟健康老師約下週一第八節，和英文老師協調後，我也獲得授權。</p>

<p>上個禮拜一，11月15日，童軍課。那時，童軍老師問「畢旅要帶什麼？」有個很皮的同學回覆「保險套！」老師的回覆竟然不是：「你們這年紀，不要想做怪怪的事。」反而回覆了：「幹嘛帶保險套！你們是男生住同一間欸！你要把你那根放到別人那裡喔？會碰到大便欸！好噁心！我不敢想像！」<br/>
同性性行為，學校絕不會教的課題。 男性同性性行為包括肛交、口交等等，保險套可預防性病、因清腸不完整而觸碰排泄物等。對，清腸，就是把人體內的排泄物清除，所以不會有「會碰到大便欸！」的問題。<br/>
不要凸顯你的無知，你好歹是童軍老師，以前還教過公民，是吧？</p>

<p>再來是星期二，有位同學戴著彩虹口罩，公民老師問：「你支持多元成家喔？」<br/>
我必須說，當下我想的跟公民老師一樣，看到彩虹，第一件事就是想到 LGBTQ+<br/>
那位同學沒有回覆，反而其他同學笑了：「哈哈哈！多元成家！」<br/>
公民老師補了句「我支持啊！我有投公投。」<br/>
多元成家，是更自由的愛。<br/>
不知道為何喜歡同性，但是「愛不分性別，只要相愛，那就是愛。」<br/>
別以多數的「異性戀」來貶低我們，彼此的尊重包容，才是真正的「愛」。</p>

<p>再來是這週二放學，我跟某位同學走回家。我一路上跟他說著同性戀的事，因為他知道我是雙性戀，也知道我喜歡誰 （P.S. 單純喜歡，也沒想說什麼，發自內心的，但我也知道他不會喜歡我。）<br/>
「你怎麼會喜歡男生啊，我還是搞不懂欸！」『就跟你喜歡女生一樣，就有那種感覺。』<br/>
「我沒辦法理解，我性向正常。」『那難道我不正常嗎？』<br/>
「對，這個時代同性戀就是不正常。我們嘴上說支持，內心還是覺得奇怪。不然你隨便去問一個路人。」<br/>
那時，我噴了一滴淚，沒說話。<br/>
「你在幹嘛？你生氣囉？」『……』「不是啊，我只是陳述事實而已。」<br/>
我們就繼續走著。<br/>
「不然你說為什麼你會喜歡 OOO 嘛？」『我不知道。』「……」<br/>
我嘆口氣，略帶哽咽開口：『我已經為這些事又哭了好幾次了，我沒告訴我家人，因為我很害怕……我也不敢說出去，因為……同性戀怕的就是對方不是同性戀。』「本來就是啊！」『我一直藏著，我真的很害怕……』<br/>
我們走到了他家，我的腳步緩緩停下，他走到我面前，跟我說了：「好啦，對不起！我支持你！」『嗯。』<br/>
「拜拜！」『拜拜！』<br/>
我就看著他走進公寓，自己又繼續往家的方向走去。</p>

<p>這些是我最近的親身經歷，而這次處了這些，我還想討論「性別平權 Gender Equality」<br/>
最近打開 Facebook，時常滑到 LGBTQ、TGEEA 等專頁（TGEEA 是台灣性平教育協會的簡寫）<br/>
這讓我更了解，也更想了解關於性別平等的相關問題。</p>

<p>那再回來問問，你更了解 LGBTQ+了嗎？<br/>
你支持 LGBTQ+嗎？為什麼？人權？朋友？我的這篇文章？還是什麼？

<p>謝謝你們看到這邊，我沒能力改變全世界，<br/>
但我希望，看到這篇文的人，能更尊重 LGBTQ+群體，讓「愛」不被性別而拘束！<br/>
「勇敢為自己所愛的，珍惜自己所有的。」<br/>
「愛不分性別，只要相愛，那就是愛。」</p>

